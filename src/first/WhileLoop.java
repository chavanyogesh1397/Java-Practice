package first;

public class WhileLoop 
{

	public static void main(String[] args) 
	{
		int i =1;
		int j=1;
		while(i<=10)
		{
			if(i%2==1)
			{
			System.out.println("Even No."+ i);
			}
			i++;
		}
		while(j<=10)
		{
			if(j%2==0)
			{
			System.out.println("Odd"+ j);
			}
			j++;
		}
		
	}

}
